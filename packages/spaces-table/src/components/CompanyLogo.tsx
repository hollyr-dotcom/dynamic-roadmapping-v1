const FALLBACK_COLOR = '#AEB2C0'

function hashName(name: string): number {
  let h = 0
  for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) | 0
  return Math.abs(h)
}

// --- Background colours ---

const BG_DEFAULT = '#F1F2F5'
const BG_ROW_HOVER = '#E9EAEF'
const BG_TAG_HOVER = '#E0E1E6'

// --- Component ---

interface CompanyLogoProps {
  name: string
  size?: number
  showName?: boolean
  onClick?: () => void
  hovered?: boolean
  /** Render just the icon with no background pill or tooltip — for use inside grouped containers */
  inline?: boolean
  /** Suppress the tooltip — for use when the parent provides its own tooltip */
  noTooltip?: boolean
}

export function CompanyLogo({ name, size = 32, showName, onClick, hovered, inline, noTooltip }: CompanyLogoProps) {
  const [tagHovered, setTagHovered] = useState(false)
  const brand = COMPANY_DATA[name]
  const iconColor = brand?.color ?? FALLBACK_COLOR
  const svgPath = brand?.svgPath ?? FALLBACK_SHAPES[hashName(name) % FALLBACK_SHAPES.length]
  const iconSize = Math.min(Math.round(size * 0.5), 20)

  const icon = (
    <svg width={inline ? size : iconSize} height={inline ? size : iconSize} viewBox="0 0 24 24" fill={iconColor} style={{ display: 'block', flexShrink: 0 }}>
      <path d={svgPath} />
    </svg>
  )

  // Inline mode: bare icon, no background, no tooltip
  if (inline) return icon

  const bg = tagHovered ? BG_TAG_HOVER : hovered ? BG_ROW_HOVER : BG_DEFAULT

  const Tag = onClick ? 'button' : 'span'

  const content = (
    <Tag
      {...(onClick ? { onClick: (e: React.MouseEvent) => { e.stopPropagation(); onClick() } } : {})}
      onMouseEnter={() => setTagHovered(true)}
      onMouseLeave={() => setTagHovered(false)}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: showName ? 'flex-start' : 'center',
        gap: showName ? 6 : 0,
        height: size,
        width: showName ? undefined : size,
        padding: showName ? `0 ${size * 0.3}px 0 ${size * 0.2}px` : 0,
        borderRadius: 8,
        background: noTooltip ? 'transparent' : bg,
        border: 'none',
        cursor: onClick ? 'pointer' : 'default',
        flexShrink: 0,
        transition: 'background 150ms ease',
      }}
    >
      {icon}
      {showName && (
        <span style={{ fontSize: 12, fontFamily: 'Noto Sans, sans-serif', color: '#222428', whiteSpace: 'nowrap' }}>
          {name}
        </span>
      )}
    </Tag>
  )

  if (noTooltip) return content

  return (
    <Tooltip>
      <Tooltip.Trigger asChild>
        {content}
      </Tooltip.Trigger>
      <Tooltip.Content side="top" sideOffset={4}>{name}</Tooltip.Content>
    </Tooltip>
  )
}
